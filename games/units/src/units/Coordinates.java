package units;

public class Coordinates {
    int x, y, maxX, maxY, minX, minY;

    public Coordinates(int x, int y, int maxX, int maxY, int minX, int minY) {
        this.x = x;
        this.y = y;
        this.maxX = maxX;
        this.maxY = maxY;
        this.minX = minX;
        this.minY = minY;
    }
}
